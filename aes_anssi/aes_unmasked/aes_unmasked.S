/************************************************************************
* SubBytes : It modifies only state
* r3 = @state
* r8 = @sbox
* r5 = internal counter
* r4, r7 = local variables
*/

SubBytes LABEL
   mov r5, #0x10
   ldr r8, =sbox
loop_SubBytes LABEL
   ldr r4,[r3]
   and r4, #0xFF
   add r4, r8
   ldrb r7, [r4]
   strb r7, [r3],#01
   subs r5,#1
   BNE  loop_SubBytes
   sub r3, #0xF
   bx LR



/************************************************************************
* ShiftRows : It modifies only state
* r3 = @state
* r4, r5, r6, r7 = local variables
*
*/

ShiftRows LABEL						// r3 = @state +1 not used : r8, r9, r10, r11, r12
	ldr r4, [r3], #0x4				// load s10
	ldr r5, [r3], #0x4				// load s11
	ldr r6, [r3], #0x4				// load s12
	ldr r7, [r3]					// load s13

	//ldm r3, {r4, r5, r6, r7} ne fonctionne pas --> fault handler
	sub r3, #0xC

	strb r5,[r3], #0x4
	strb r6,[r3], #0x4
	strb r7,[r3], #0x4
	strb r4,[r3], #0x4

	sub r3, #0xF
	ldr r4, [r3], #0x4
	ldr r5, [r3], #0x4
	ldr r6, [r3], #0x4
	ldr r7, [r3]

	sub r3, #0xC
	strb r6,[r3], #0x4
	strb r7,[r3], #0x4
	strb r4,[r3], #0x4
	strb r5,[r3]

