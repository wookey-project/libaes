	ldrb r4, [r3], #0x4
	ldrb r5, [r3], #0x4
	ldrb r6, [r3], #0x4
	ldrb r7, [r3]

	sub r3, #0xC
	strb r7,[r3], #0x4
	strb r4,[r3], #0x4
	strb r5,[r3], #0x4
	strb r6,[r3]

	sub r3, #0xF
	bx LR

/************************************************************************
* MixColumns: It modifies only state
*
* r3 = @state
* r5 = internal counter
* r4, r7, r8, r10, r11 = local variables
*
*/

MixColumns LABEL 				//r3 = @state ; r6 used in lnot used : r12
    push {LR}
	mov r5,#0x4					// ctr (+1 for each column)
MixColumns_Loop LABEL			// for each column
	ldrb r8, [r3], #0x1			// a1
	eor r4, r8, #0x0			//

	ldrb r11, [r3], #0x1		// a2
	eor r4, r4, r11

	ldrb r10, [r3], #0x1		// a3
	eor r4, r4, r10

	ldrb r7, [r3]				// a4
	eor r4, r4, r7
	sub r3, #0x3				// come back to the first byte adr

	push {r7}

	eor r7,r8,r11				// a1+a2
	bl Mult	 					// 2*(a1+a2)
	eor r7, r8					// 2*(a1+a2)+a1
	eor r7,r4					// 2*(a1+a2)+a1+t
	strb r7, [r3], #0x1

	eor r7, r11, r10				// a2+a3
	bl Mult						// 2*(a2+a3)
	eor r7, r11					// 2*(a2+a3)+a2
	eor r7, r4					// 2*(a2+a3)+a2+t
	strb r7, [r3], #0x1

	pop {r11}
	eor r7, r10, r11			// a3+a4
	bl Mult						// 2*(a3+a4)
	eor r7, r10					// 2*(a3+a4)+a3
	eor r7, r4					// 2*(a3+a4)+a3+t
	strb r7, [r3], #0x1

	eor r7, r8, r11				// a1+a4
	bl Mult						// 2*(a1+a4)
	eor r7, r11					// 2*(a1+a4)+a4
	eor r7, r4					// 2*(a1+a4)+a4+t
	strb r7, [r3], #0x1

	subs r5,#0x1
	bne MixColumns_Loop
	subs r3,#0x10
	pop {PC}




